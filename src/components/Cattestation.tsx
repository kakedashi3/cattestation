export function Cattestation() {
  const { isConnected, address } = useAccount();
  const [data, setData] = useState<any>();
  const [message, setMessage] = useState<string>("");
  const chainId = useChainId();
  const { config, error } = usePrepareContractWrite({
    address: CONTRACT_ADDRESS,
    abi: CattestationABI,
    functionName: "pet",
    args: [
      address!,
      message,
      {
        v: data?.signature.raw.v,
        r: (`0x` + data?.signature.raw.r) as Address,
        s: (`0x` + data?.signature.raw.s) as Address,
      },
    ],
  });

  // const supportsNFC = "NDEFReader" in window;

  const { write, data: result, error: txError } = useContractWrite(config);
  const { isLoading, isSuccess } = useWaitForTransaction({
    hash: result?.hash,
  });

  async function onClick() {
    const domain = {
      name: "Cattestation",
      version: "1",
      chainId,
      verifyingContract: CONTRACT_ADDRESS,
    };

    // The named list of all type definitions
    const types = {
      Meow: [
        {
          type: "address",
          name: "human",
        },
        {
          type: "string",
          name: "message",
        },
      ],
    };

    const value = {
      human: address,
      message,
    };

    const command = {
      name: "sign",
      keyNo: 1,
      typedData: {
        domain,
        types,
        value,
      },
    };

    const data = await execHaloCmdWeb(command);

    console.log(data);
    setData(data);
  }

  // if (!supportsNFC) {
  //   return (
  //     <p className="bg-red-100 text-black p-2 rounded-lg text-center">
  //       This device does not support NFC.
  //       <br />
  //       Please try on a recent mobile device!
  //     </p>
  //   );
  // }

  if (isSuccess) {
    return (
      <div className="space-y-2">
        <p>Meowza! Your cattestation has been minted!</p>
        <Button className="w-full" asChild>
          <a
            target="_blank"
            href={`https://arbitrum.easscan.org/address/${address}`}
          >
            Check Attestation on EASscan
          </a>
        </Button>
        <Button className="w-full" asChild>
          <a target="_blank" href={`https://opensea.io/${address}`}>
            Check NFT on Opensea
          </a>
        </Button>
      </div>
    );
  }

  if (write) {
    return (
      <>
        <div>{error?.message ?? txError?.message}</div>
        <Button
          className="w-full"
          onClick={() => write()}
          disabled={!isConnected || isLoading}
        >
          Mint Cattestation
        </Button>
      </>
    );
  }

  return (
    <>
      <input
        className="border w-full"
        placeholder="Message"
        value={message}
        onChange={(e) => setMessage(e.target.value)}
      />
      <Button className="w-full" onClick={onClick} disabled={!isConnected}>
        Verify Cattestation {!isConnected && "(connect wallet)"}
      </Button>
    </>
  );
}
